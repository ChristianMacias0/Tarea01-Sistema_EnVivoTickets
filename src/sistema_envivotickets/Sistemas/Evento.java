package sistema_envivotickets;


public class Evento {

    private int id;
    private String nombre;
    private String tipo;
    private String descripcion;



    //Getters

    public int getId() {
        return id;
    }
    public String getNombre() {
        return nombre;
    }
    public String getTipo() {
        return tipo;
    }
    public String getDescripcion() {
        return descripcion;
    }

}
